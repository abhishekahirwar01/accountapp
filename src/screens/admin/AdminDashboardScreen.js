import React, { useState, useEffect } from 'react';
import {
  View,
  Text,
  ScrollView,
  TouchableOpacity,
  ActivityIndicator,
  StyleSheet,
  RefreshControl,
  Alert,
} from 'react-native';
import { SafeAreaView } from 'react-native-safe-area-context';
import { BASE_URL } from '../../config';
import AsyncStorage from '@react-native-async-storage/async-storage';
import Icon from 'react-native-vector-icons/MaterialCommunityIcons';
import Header from '../../components/layout/Header';
import BottomNav from '../../components/layout/BottomNav';
import UpdateNotification from '../../components/notifications/UpdateNotification';
import UpdateNotificationBadge from '../../components/notifications/UpdateNotificationBadge';

// Import your screens
import ClientManagementScreen from './ClientManagementScreen';
import ClientDetailScreen from './ClientDetailScreen';
import CompaniesScreen from './CompaniesScreen';
import AnalyticsScreen from './AnalyticsScreen';
import SettingsScreen from './SettingsScreen';

export default function AdminDashboardScreen({ navigation }) {
  const [currentTab, setCurrentTab] = useState('Dashboard');
  const [clients, setClients] = useState([]);
  const [companies, setCompanies] = useState([]);
  const [isLoading, setIsLoading] = useState(true);
  const [refreshing, setRefreshing] = useState(false);

  useEffect(() => {
    if (currentTab === 'Dashboard') {
      fetchData();
    }
  }, [currentTab]);

  const fetchData = async () => {
    setIsLoading(true);
    try {
      await Promise.all([
        fetchClients(),
        fetchCompanies()
      ]);
    } catch (error) {
      Alert.alert('Error', 'Failed to load dashboard data');
    } finally {
      setIsLoading(false);
      setRefreshing(false);
    }
  };

  const fetchClients = async () => {
    try {
      const token = await AsyncStorage.getItem('token');
      if (!token) {
        Alert.alert('Error', 'Please login again');
        navigation.navigate('Login');
        return;
      }
      
      const res = await fetch(`${BASE_URL}/api/clients`, {
        headers: { "Authorization": `Bearer ${token}` }
      });
      
      if (!res.ok) {
        if (res.status === 401) {
          await AsyncStorage.removeItem('token');
          Alert.alert('Session Expired', 'Please login again');
          navigation.navigate('Login');
          return;
        }
        const errorData = await res.json();
        throw new Error(errorData.message || "Failed to fetch clients.");
      }
      
      const data = await res.json();
      setClients(data);
    } catch (error) {
      console.error('Error fetching clients:', error);
      Alert.alert('Error', error.message || 'Failed to fetch clients');
      throw error;
    }
  };

  const fetchCompanies = async () => {
    try {
      const token = await AsyncStorage.getItem('token');
      if (!token) throw new Error("Authentication token not found.");
      
      const res = await fetch(`${BASE_URL}/api/companies/all`, {
        headers: { "Authorization": `Bearer ${token}` }
      });
      
      if (!res.ok) {
        const errorData = await res.json();
        throw new Error(errorData.message || "Failed to fetch companies.");
      }
      
      const data = await res.json();
      setCompanies(data);
    } catch (error) {
      console.error('Error fetching companies:', error);
      throw error;
    }
  };

  const onRefresh = () => {
    setRefreshing(true);
    fetchData();
  };

  // Calculate stats from local data (same as Next.js)
  const totalClients = clients.length;
  const activeClients = clients.filter(c => c.status === 'Active').length;
  const totalCompanies = companies.length;
  
  // Mock data for KPIs (same as Next.js)
  const totalTransactions = 1452;
  const pendingInvoices = 23;

  const kpiData = [
    { 
      title: 'Total Clients', 
      value: totalClients.toString(), 
      change: `+${Math.floor(Math.random() * 5)} this month`, 
      icon: 'account-group',
      iconBg: '#E3F2FD',
      iconColor: '#2196F3'
    },
    { 
      title: 'Companies Managed', 
      value: totalCompanies.toString(), 
      change: '+5 this month', 
      icon: 'office-building',
      iconBg: '#E8F5E8',
      iconColor: '#4CAF50'
    },
    { 
      title: 'Total Transactions', 
      value: totalTransactions.toLocaleString(), 
      change: '+120 this week', 
      icon: 'database',
      iconBg: '#F3E5F5',
      iconColor: '#9C27B0'
    },
    { 
      title: 'Pending Invoices', 
      value: pendingInvoices.toString(), 
      change: 'Across all clients', 
      icon: 'file-document',
      iconBg: '#FFF3E0',
      iconColor: '#FF9800'
    },
  ];

  const getTrendIcon = (change) => {
    if (change.startsWith('+')) {
      return 'trending-up';
    }
    return 'trending-down';
  };

  const getTrendColor = (change) => {
    if (change.startsWith('+')) {
      return '#4CAF50';
    }
    return '#F44336';
  };

  const renderDashboardContent = () => {
    if (isLoading) {
      return (
        <View style={styles.loadingContainer}>
          <ActivityIndicator size="large" color="#2196F3" />
          <Text style={styles.loadingText}>Loading dashboard data...</Text>
        </View>
      );
    }

    return (
      <ScrollView
        style={styles.scrollView}
        refreshControl={
          <RefreshControl refreshing={refreshing} onRefresh={onRefresh} />
        }
      >
        {/* Header Section */}
        <View style={styles.header}>
          <View style={styles.headerContent}>
            <Text style={styles.title}>Master Admin Dashboard</Text>
            <Text style={styles.subtitle}>
              Overview of your accounting software platform
            </Text>
          </View>
          <View style={styles.badgeContainer}>
            <UpdateNotificationBadge />
          </View>
        </View>

        {/* KPI Cards Grid - Non-clickable (same as Next.js) */}
        <View style={styles.kpiGrid}>
          {kpiData.map((kpi, index) => (
            <View
              key={kpi.title}
              style={styles.kpiCard}
            >
              <View style={styles.kpiContent}>
                <View style={styles.kpiTextContainer}>
                  <Text style={styles.kpiTitle}>{kpi.title}</Text>
                  <Text style={styles.kpiValue}>{kpi.value}</Text>
                  <View style={styles.kpiChangeContainer}>
                    <Icon
                      name={getTrendIcon(kpi.change)}
                      size={16}
                      color={getTrendColor(kpi.change)}
                    />
                    <Text style={[styles.kpiChange, { color: getTrendColor(kpi.change) }]}>
                      {kpi.change}
                    </Text>
                  </View>
                </View>
                <View style={[styles.kpiIconContainer, { backgroundColor: kpi.iconBg }]}>
                  <Icon name={kpi.icon} size={24} color={kpi.iconColor} />
                </View>
              </View>
            </View>
          ))}
        </View>

        {/* Update Notifications */}
        <View style={styles.notificationSection}>
          <UpdateNotification />
        </View>
      </ScrollView>
    );
  };

  const renderContent = () => {
    switch (currentTab) {
      case 'Dashboard':
        return (
          <SafeAreaView style={styles.container}>
            {renderDashboardContent()}
          </SafeAreaView>
        );

      case 'Clients':
        return <ClientManagementScreen navigation={navigation} />;
      case 'ClientDetail':
        return <ClientDetailScreen navigation={navigation} />;
      case 'Companies':
        return <CompaniesScreen navigation={navigation} />;
      case 'Analytics':
        return <AnalyticsScreen navigation={navigation} />;
      case 'Settings':
        return <SettingsScreen navigation={navigation} />;

      default:
        return (
          <View style={styles.center}>
            <Text style={styles.comingSoonText}>{currentTab} Screen Coming Soon...</Text>
          </View>
        );
    }
  };

  return (
    <View style={styles.mainContainer}>
      {/* Header only on Dashboard */}
      {currentTab === 'Dashboard' && <Header username="Master Admin" role="master" />}

      {/* Tab-wise Content */}
      <View style={styles.contentContainer}>
        {renderContent()}
      </View>

      {/* Bottom Navigation */}
      <BottomNav 
        role="master" 
        currentTab={currentTab}
        onTabChange={tab => setCurrentTab(tab)} 
      />
    </View>
  );
}

